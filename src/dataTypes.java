public class dataTypes {
    public static void main(String[] args) {
        /* Data Types
        Primitive Data Types
        * byte
        * short
        * int
        * long
        * char
        * float
        * double
        * boolean
        */
        byte a = 1;
        short b = 1;
        int x = 1;
        long y = 2L;
        char c = 'a';
        float f =  2.0f;
        double d = 2.0d;

        /*Non-Primitive Data Types
        * String
        * Array
        * Classes
        */
        String str = "This is a string";

        int [] arr1 = new int[2];
        arr1[0] = 2;
        arr1[1] = 2;

        int [] arr2 = {2,2};
    }
}
